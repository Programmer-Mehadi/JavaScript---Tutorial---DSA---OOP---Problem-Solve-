// what is mutable?
// ans : mutable means we can change the value of the variable

// what is immutable?
// ans : immutable means we can not change the value of the variable


// example of mutable
let a = 10;
a = 20;
console.log(a);

// example of immutable
const b = 10;
console.log(b);
b = 20;
console.log(b);